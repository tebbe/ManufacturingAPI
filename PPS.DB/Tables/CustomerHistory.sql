CREATE TABLE [dbo].[CustomerHistory] (
    [Id]              INT           IDENTITY(1,1) NOT NULL,
    [CustomerId]      INT           NOT NULL,
    [CustomerName]    VARCHAR (50)  NOT NULL,
    [CustomerCode]            INT           NOT NULL,
    [CustomerAddress] VARCHAR (300) NOT NULL,
    [CustomerMobile]          VARCHAR (16)  NULL,
    [CustomerPhone]           VARCHAR (16)  NULL,
    [OwnerName]    VARCHAR (100)  NOT NULL,
	[OwnerMobile]          VARCHAR (16)  NULL,
    [OwnerPhone]           VARCHAR (16)  NULL,
    [OwnerBirthDate] DATE NULL, 
    [ContactPersonName] VARCHAR(100) NULL, 
    [ContactPersonMobile] VARCHAR(16) NULL, 
    [PrimaryContactNo] VARCHAR(16) NULL, 
    [Village]         VARCHAR (100) NULL,
    [PostOfficeId]    INT           NULL,
    [Email]           VARCHAR (50)  NULL,
    [AreaId]          INT           NULL,
    [EmployeeId]          INT           NULL,
    [CustomerTypeId]  INT           NULL,
    [AccountHeadId]   INT			NULL, 
	[CustomerStatusId] INT NULL,
	[CreatedBy]     INT           NULL,
    [CreatedOn]     DATETIME      NULL,
	[UpdatedBy]     INT           NULL,
    [UpdatedOn]     DATETIME      NULL,
    CONSTRAINT [HistoryCustomer_Primary_Key] PRIMARY KEY CLUSTERED ([Id] ASC),
    CONSTRAINT [HistoryCustomer_Area_Foreign_Key] FOREIGN KEY ([AreaId]) REFERENCES [dbo].[Area] ([Id]),
    CONSTRAINT [HistoryCustomer_PostOffice_Foreign_Key] FOREIGN KEY ([PostOfficeId]) REFERENCES [dbo].[PostOffice] ([Id]),
    CONSTRAINT [HistoryCustomer_AccountHead_Foreign_Key] FOREIGN KEY ([AccountHeadId]) REFERENCES [dbo].[AccountHead] ([Id]),
    CONSTRAINT [HistoryCustomer_CustomerStatus_Foreign_Key] FOREIGN KEY ([CustomerStatusId]) REFERENCES [dbo].[CustomerStatus] ([Id])
);

